import numpy as np

# =====================================================================
# 核心概念 1：基础聚合与“降维打击” (axis参数是灵魂)
# =====================================================================
print("--- 1. 基础聚合与降维 ---")
# 假设这是3个学生(行)，在2门课(列：数学, Python)上的成绩
scores = np.array([
    [80, 90],  # 学生A
    [60, 95],  # 学生B
    [100, 85]  # 学生C
])

# 【底层逻辑】：如果不指定 axis，NumPy会把整个数组“压扁”成一维，然后统计。
print("所有分数的总和 (sum):", np.sum(scores)) 

# 【核心难点：axis参数】
# 面试常考：axis=0 代表沿着行的方向“往下压”，把不同行挤成一行。也就是“求每一列的统计值”。
# 在这个例子中：向下压，就是求所有学生在“数学”上的最高分，和在“Python”上的最高分。
print("每门课的最高分 (max, axis=0):", np.max(scores, axis=0)) # 输出: [100  95]

# axis=1 代表沿着列的方向“往右压”，把不同列挤成一列。也就是“求每一行的统计值”。
print("每个学生的总分 (sum, axis=1):", np.sum(scores, axis=1)) # 输出: [170 155 185]

# 【实战算法利器：argmin / argmax】
# argmax 不返回最大值本身，而是返回最大值所在的“索引(下标)”。这在刷题时极为常用！
# 比如：我们想知道“数学”单科状元是哪个学生（第几行）？
math_scores = scores[:, 0] # 提取数学成绩 [80, 60, 100]
best_student_idx = np.argmax(math_scores)
print(f"数学最高分所在的索引 (argmax): {best_student_idx} (即学生C)")


# =====================================================================
# 核心概念 2：描述数据的“脾气” (mean, var, std)
# =====================================================================
print("\n--- 2. 数据的波动与离散程度 ---")
# 假设这是两位程序员最近5天的代码Bug数量
coder_x = np.array([5, 5, 5, 5, 5])   # 极其稳定
coder_y = np.array([0, 10, 0, 10, 5]) # 情绪化编程

# 1. 均值 (mean): 算术平均数
print("X的平均Bug数:", np.mean(coder_x)) # 5.0
print("Y的平均Bug数:", np.mean(coder_y)) # 5.0

# 【底层逻辑：方差 var (Variance)】
# 两人平均数一样，怎么衡量谁更不稳定？
# 思路：让每天的Bug数减去平均值，看看偏离了多少。
# 但是，有正有负（比如Y的偏差是 -5, 5, -5, 5, 0），直接相加会互相抵消变成0！
# 费曼技巧：为了不让正负抵消，我们把偏差统统“平方”，然后再求平均。这就是方差！
print("X的方差 (var):", np.var(coder_x)) # 0.0 (完全没波动)
print("Y的方差 (var):", np.var(coder_y)) # 20.0 (波动很大)

# 【底层逻辑：标准差 std (Standard Deviation)】
# 方差有一个致命问题：单位被平方了！比如原来的单位是"个Bug"，方差的单位变成了"个Bug的平方"，这在现实中无法解释。
# 费曼技巧：为了把单位还原，我们把方差“开根号”，这就是标准差！
print("Y的标准差 (std):", np.std(coder_y)) # 根号20 ≈ 4.47 (说明Y每天的Bug数平均偏离基准线4.47个)


# =====================================================================
# 核心概念 3：前缀和缓存 (cumsum)
# =====================================================================
print("\n--- 3. 算法常客：前缀和 (cumsum) ---")
daily_income = np.array([100, 200, 300, 400])
# 【底层逻辑】：累加和。第i个位置的值，是原数组前i个元素的总和。
# 算法题中，常用来在 O(1) 时间内求任意区间的和。
print("每日累计收入 (cumsum):", np.cumsum(daily_income)) 
# 输出: [ 100  300  600 1000] (第一天100，前两天300...)


# =====================================================================
# 核心概念 4：数据间的“暧昧关系” (cov, corrcoef)
# =====================================================================
print("\n--- 4. 相关性分析 ---")
# 假设我们统计了5个学生的“刷题量”和“面试Offer数”
leet_code_solved = np.array([10, 50, 100, 200, 300])
offers_received  = np.array([ 0,  1,   2,   4,   5])

# 【底层逻辑：协方差 cov (Covariance)】
# 衡量两个变量是否“同向变化”。你涨我也涨，就是正数；你涨我跌，就是负数。
# 注意：np.cov 默认返回一个协方差矩阵 (Covariance Matrix)。
# 矩阵的主对角线是各自的方差，非对角线是两者的协方差。
covariance_matrix = np.cov(leet_code_solved, offers_received)
print("刷题与Offer的协方差:\n", covariance_matrix)
# 痛点：协方差的数值大小受原始数据单位影响很大（比如刷题量几百，Offer数几个），
# 算出来的数值（比如 235）很难直观判断关系到底有多“强”。

# 【底层逻辑：相关系数 corrcoef (Correlation Coefficient)】
# 费曼技巧：相关系数就是“脱了马甲”的协方差！
# 它强行把协方差按比例压缩到了 [-1, 1] 的区间内。
# 1 代表完美的正相关（完全同涨同跌），-1 代表完美的负相关，0 代表没关系。
correlation_matrix = np.corrcoef(leet_code_solved, offers_received)
print("\n刷题与Offer的相关系数矩阵:\n", correlation_matrix)
# 输出矩阵的右上角和左下角的值接近 0.99，说明刷题量和Offer数呈极其强烈的正相关！