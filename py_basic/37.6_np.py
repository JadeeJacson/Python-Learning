import numpy as np

# =====================================================================
# 核心概念 1：基础数学函数 (全是“向量化”操作，拒绝 for 循环)
# =====================================================================
print("--- 1. 基础数学函数 ---")
arr = np.array([1.0, 2.0, 3.0])

# 【底层逻辑】：
# exp(x) 是自然底数 e (约2.718) 的 x 次方，代表“疯狂的指数级增长”。
# log(x) 是 exp 的反面，问的是“要增长到 x，需要多久？”。它能把极大的数字压缩变小。
print("指数爆炸 (exp):", np.exp(arr)) 
print("降维打击 (log):", np.log(arr)) # 默认是自然对数 ln
print("开平方根 (sqrt):", np.sqrt(arr))

# 三角函数 (sin, cos, tan) 处理的是“周期性变化”。
# ⚠️ 注意：NumPy里的三角函数，输入必须是“弧度(radian)”，不能直接传角度(如90度)！
angles = np.array([0, np.pi/2, np.pi]) # 0度, 90度, 180度
print("正弦 (sin):", np.sin(angles)) # 输出 [0. 1. 0.] (浮点误差会导致最后一个极小)


# =====================================================================
# 核心概念 2：向量间的“互动” (dot, inner, outer)
# =====================================================================
print("\n--- 2. 向量的乘法哲学 ---")
v1 = np.array([1, 2, 3])
v2 = np.array([4, 5, 6])

# 【底层逻辑：内积/点积 (dot / inner)】
# 物理意义：衡量两个向量有多“相似”，或者说“同向”。
# 算法：对应位置相乘，然后全部加起来。 1*4 + 2*5 + 3*6 = 32
print("点积 (dot):", np.dot(v1, v2)) 
# 一维数组中，np.inner 和 np.dot 效果完全一样。
print("内积 (inner):", np.inner(v1, v2)) 

# 【底层逻辑：外积 (outer)】
# 物理意义：“排列组合”制造矩阵。用 v1 的每一个元素去乘以 v2 的整体。
# 结果必然是一个二维矩阵 (长3 x 宽3)
print("外积 (outer):\n", np.outer(v1, v2))


# =====================================================================
# 核心概念 3：线性代数核心 np.linalg (矩阵运算)
# =====================================================================
print("\n--- 3. 线性代数 (np.linalg) ---")

A = np.array([[2, 1], 
              [1, 3]])
B = np.array([[1, 2], 
              [3, 4]])

# 1. 矩阵乘法 (matmul 或者 @)
# 【底层逻辑】：矩阵A乘以矩阵B，不是简单的位置相乘！
# 它是A的每一行去和B的每一列做“点积”。代表着“两次空间变换的叠加”。
print("矩阵乘法 (matmul):\n", np.matmul(A, B))
print("矩阵乘法语法糖 (@):\n", A @ B) # 现代Python极力推荐这种写法！

# 2. 矩阵求逆 (inv) 和 求解方程组 (solve)
# 假设有方程组：
# 2*x + 1*y = 5
# 1*x + 3*y = 5
# 我们要求 x, y。写成矩阵形式就是 A @ X = b
b = np.array([5, 5])

# 【底层逻辑：求逆 inv】
# “逆矩阵”就像是游戏里的“撤销键”。矩阵A把数据变换了，A的逆矩阵能把它变回来。
# 数学解法：X = A的逆 @ b
A_inv = np.linalg.inv(A)
print("\n用求逆法求方程组的解:", A_inv @ b) 

# 【实战最佳实践：solve】
# 面试重点：真实开发中【极其不建议】先求逆再乘法！求逆既慢又容易丢失精度。
# np.linalg.solve 内部用了更聪明的算法（高斯消元法等）直接求出答案。
print("用 solve 直接求解 (推荐):", np.linalg.solve(A, b)) # 输出 [2. 1.]

# 3. 向量/矩阵的模长 (norm)
# 【底层逻辑】：就是求“长度”或者“距离”。
# 默认求 L2 范数（也就是勾股定理算出的直线距离）。
point = np.array([3, 4])
print("\n向量 [3,4] 的长度 (norm):", np.linalg.norm(point)) # 必须是5 (勾股定理)

# 4. 特征值与特征向量 (eig) & 奇异值分解 (svd)
# 【底层逻辑：eig】矩阵代表扭曲，但在扭曲过程中，总有几个特殊方向的向量“只被拉长缩短，绝不偏离原方向”。
# 这就是特征向量！拉伸的倍数就是特征值。它是矩阵的“灵魂”。
eigenvalues, eigenvectors = np.linalg.eig(A)
print("\nA的特征值:\n", eigenvalues)

# 【底层逻辑：svd】把任何一块复杂难啃的矩阵“肉”，剔骨分离成三块：
# 旋转矩阵 (U) + 拉伸力度 (S) + 再次旋转矩阵 (V)。常用于数据降维和压缩！
U, S, V = np.linalg.svd(A)
print("SVD 分解出的奇异值 (骨架大小):", S)